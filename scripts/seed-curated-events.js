const TZ_OFFSET = '+02:00';
const RECURRING_WINDOW_DAYS = 180;

function toIso(date, hhmm) {
  return new Date(`${date}T${hhmm}:00${TZ_OFFSET}`).toISOString();
}

function isoDateKey(date) {
  const year = date.getFullYear();
  const month = String(date.getMonth() + 1).padStart(2, '0');
  const day = String(date.getDate()).padStart(2, '0');
  return `${year}-${month}-${day}`;
}

function addDays(date, days) {
  const d = new Date(date);
  d.setDate(d.getDate() + days);
  return d;
}

function firstThursdayOfMonth(year, month) {
  const d = new Date(year, month, 1);
  while (d.getDay() !== 4) {
    d.setDate(d.getDate() + 1);
  }
  return d;
}

function generateRecurringEvents({
  idPrefix,
  title,
  weekday,
  startTime,
  endTime,
  venue,
  address,
  category,
  placeType,
  sourceName,
  sourceUrl,
  ticketsUrl,
  tags,
  exclusions = [],
  description = ''
}) {
  const now = new Date();
  const start = new Date(now.getFullYear(), now.getMonth(), now.getDate());
  const end = addDays(start, RECURRING_WINDOW_DAYS);

  const events = [];
  for (let cursor = new Date(start); cursor <= end; cursor = addDays(cursor, 1)) {
    if (cursor.getDay() !== weekday) {
      continue;
    }

    const dateKey = isoDateKey(cursor);
    if (exclusions.includes(dateKey)) {
      continue;
    }

    events.push({
      id: `${idPrefix}-${dateKey}`,
      title,
      description,
      startAt: toIso(dateKey, startTime),
      endAt: toIso(dateKey, endTime),
      venue,
      address,
      category,
      placeType,
      price: 'Проверьте актуальную цену на источнике',
      currency: 'ZAR',
      sourceName,
      sourceUrl,
      ticketsUrl,
      tags,
      verified: true,
      reliabilityScore: 0.9
    });
  }

  return events;
}

