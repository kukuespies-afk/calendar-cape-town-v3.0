export function buildCuratedEvents() {
  return [...fixedEvents(), ...recurringEvents(), ...monthlyFirstThursdayEvents(), ...extendedFixedEvents(), ...extendedRecurringEvents()];
}

export async function runCuratedSeed({ mode = 'replace' } = {}) {
  await initRepository();
  const curated = buildCuratedEvents();
  const result = mode === 'upsert' ? await upsertEvents(curated) : await replaceEvents(curated);
  return {
    ok: true,
    mode,
    seededEvents: curated.length,
    totalEventsInStorage: result.length,
    categories: [...new Set(curated.map((event) => event.category))],
    placeTypes: [...new Set(curated.map((event) => event.placeType))]
  };
}

const isDirectRun = process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href;
if (isDirectRun) {
  const mode = process.argv.includes('--upsert') ? 'upsert' : 'replace';
  const summary = await runCuratedSeed({ mode });
  console.log(JSON.stringify(summary, null, 2));
}
